package app.beans

class PropertyAddress {
	
	private String number;
	private String direction;
	private String street;
	private String lotUnit;
	private String city;
	private String state;
	private String zip;
	private String fullAddress;
	private String searchNumber;
	private String zipCode5;
	
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getDirection() {
		return direction;
	}
	public void setDirection(String direction) {
		this.direction = direction;
	}
	public String getStreet() {
		return street;
	}
	public void setStreet(String street) {
		this.street = street;
	}
	public String getLotUnit() {
		return lotUnit;
	}
	public void setLotUnit(String lotUnit) {
		this.lotUnit = lotUnit;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getZip() {
		return zip;
	}
	public void setZip(String zip) {
		this.zip = zip;
	}
	public String getFullAddress() {
		return fullAddress;
	}
	public void setFullAddress(String fullAddress) {
		this.fullAddress = fullAddress;
	}
	public String getSearchNumber() {
		return searchNumber;
	}
	public void setSearchNumber(String searchNumber) {
		this.searchNumber = searchNumber;
	}
	public String getZipCode5 () {
		return zipCode5;
	}
	public void setZipCode5(String zipCode5) {
		this.zipCode5 = zipCode5;
	}
	
}
